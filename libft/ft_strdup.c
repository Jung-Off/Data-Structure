#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int i;
	char *s;

	i = 0;
	if(!(s = (char*)malloc(ft_strlen(s1) + 1)))
		return (0);

	while(s1[i])
	{
		s[i] = s1[i];
		++i;
	}
	s[i] = '\0';
	return (s);
}

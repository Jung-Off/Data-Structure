#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t i;
	char new_c;

	new_c = c;
	i = 0;
	while(i < ft_strlen(s) + 1 )
	{
		if(s[i] == c)
			return ((char*)s + i);
		++i;
	}
	return (0);
}
